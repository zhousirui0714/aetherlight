async function checkExisting() {
  const titles = ARTICLES.map((a) => a.title);
  const r = await fetch(
    `${URL}/rest/v1/knowledge_articles?select=id,title&title=in.(${titles.map(encodeURIComponent).join(",")})`,
    { headers: { apikey: KEY, Authorization: `Bearer ${KEY}` } }
  );
  if (!r.ok) {
    const t = await r.text();
    throw new Error(`Check failed ${r.status}: ${t.slice(0, 200)}`);
  }
  return await r.json();
}

