def parse_filename(name: str) -> dict:
    """从文件名提取 dynasty / author（如 '声律启蒙-清-车万育'）"""
    import re
    m = re.match(r"^(.+?)-([宋元明清东汉春秋战国西夏辽金商周秦五代十国南北朝隋唐]+)-(.+)$", name)
    if m:
        return {"title": m.group(1), "dynasty": m.group(2), "author": m.group(3)}
    return {"title": name, "dynasty": "未知", "author": "未知"}
