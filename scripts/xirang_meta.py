def to_slug(name: str) -> str:
    """生成 URL-safe slug（简化版：保留中文，转拼音可后续做）"""
    # 保留中英文 + 数字，去空格/标点
    import re
    s = re.sub(r"[\s/\\:*?\"<>|]", "-", name)
    s = s.strip("-")
    return f"xirang-{s}"


